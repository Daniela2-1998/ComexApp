    private void jButtonModificarUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarUltimoActionPerformed

        String contenedor, fechaUltimo;
        int dia, mes, año;
        Date ultimoControl;
        
        contenedor = textCodigo.getText().trim();

        dia = jDayChooser.getDay();
        mes = jMonthChooser.getMonth() + 1;
        año = jYearChooser.getYear();
        
        fechaUltimo = año + "-" + mes + "-" + dia; 
        ultimoControl = Date.valueOf(fechaUltimo);
        
        contenedoresImpl.modificarFechaUltimo(ultimoControl, contenedor);
        
    }//GEN-LAST:event_jButtonModificarUltimoActionPerformed

